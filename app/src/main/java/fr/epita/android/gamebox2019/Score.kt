package fr.epita.android.gamebox2019

class Score {
    // A timestamp showing when the game took place
    val date : String = ""
    // Id for the game that was played
    val game_id : Int = 0
    // Short name for the game that was played
    val game : String = ""
    // Name of player who played
    val player : String = ""
    // Game score. This can take only 3 possible values : 'win', 'draw' or 'loose'
    val score : String = ""
}